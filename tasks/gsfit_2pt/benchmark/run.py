"""Benchmark driver for the gsfit_2pt task."""

# ruff: noqa: E402

from __future__ import annotations

import argparse
import importlib
import json
from pathlib import Path
import subprocess
import sys

REPO_ROOT = Path(__file__).resolve().parents[3]
if str(REPO_ROOT) not in sys.path:
    sys.path.insert(0, str(REPO_ROOT))

from core.leaderboard import save_result
from core.task import BenchmarkResult

from tasks.gsfit_2pt.benchmark.metrics import benchmark_submission
from tasks.gsfit_2pt.dataset.synthetic import load_synthetic_cases
from tasks.gsfit_2pt.interface import Pion2PtGroundStateFit
from tasks.gsfit_2pt.task import Gsfit2PtTask


def load_submission(submission_name: str) -> Pion2PtGroundStateFit:
    """Import and instantiate a submission from submissions/<name>.py."""

    module = importlib.import_module(f"tasks.gsfit_2pt.submissions.{submission_name}")
    for value in module.__dict__.values():
        if (
            isinstance(value, type)
            and issubclass(value, Pion2PtGroundStateFit)
            and value is not Pion2PtGroundStateFit
        ):
            return value()
    raise ValueError(
        f"No Pion2PtGroundStateFit submission found in module '{submission_name}'."
    )


def run_validation_tests(*, skip_tests: bool) -> None:
    """Run gsfit validation tests before benchmarking unless explicitly skipped."""

    if skip_tests:
        print(
            "WARNING: Skipping pytest validation gate via --skip-tests. "
            "Benchmark results may be invalid."
        )
        return

    result = subprocess.run(
        [
            sys.executable,
            "-m",
            "pytest",
            "tasks/gsfit_2pt/tests/test_validation.py",
            "-q",
        ],
        cwd=REPO_ROOT,
        check=False,
    )
    if result.returncode != 0:
        raise SystemExit(
            "gsfit_2pt benchmark refused to run because pre-benchmark "
            "validation tests failed "
            f"(exit code {result.returncode}). "
            "Fix tests first or rerun with --skip-tests."
        )


def main() -> None:
    parser = argparse.ArgumentParser(description="Run the gsfit_2pt benchmark")
    parser.add_argument(
        "--submission",
        type=str,
        required=True,
        help="Submission module name under submissions/",
    )
    parser.add_argument(
        "--num-samples",
        type=int,
        default=24,
        help="Synthetic resamples per benchmark case",
    )
    parser.add_argument(
        "--max-resamples",
        type=int,
        default=None,
        help="Limit the number of resample refits used for the uncertainty estimate",
    )
    parser.add_argument(
        "--noise-multiplier",
        type=float,
        default=1.0,
        help="Global multiplier for the synthetic benchmark noise level",
    )
    parser.add_argument(
        "--dataset-file",
        type=Path,
        default=None,
        help="Optional .npz archive generated by scripts/generate_fake_data.py",
    )
    parser.add_argument(
        "--output-dir",
        type=Path,
        default=Path("tasks/gsfit_2pt/benchmark/results"),
        help="Directory to save the benchmark JSON result",
    )
    parser.add_argument(
        "--skip-tests",
        action="store_true",
        help=(
            "Skip pre-benchmark pytest validation "
            "(tasks/gsfit_2pt/tests/test_validation.py)"
        ),
    )
    args = parser.parse_args()

    run_validation_tests(skip_tests=args.skip_tests)
    submission = load_submission(args.submission)
    task = Gsfit2PtTask()
    if not task.validate(submission):
        raise SystemExit(
            "gsfit_2pt benchmark refused to run because the submission failed "
            f"validation. Run `pytest {task.tests_path}` before benchmarking."
        )
    cases = (
        load_synthetic_cases(args.dataset_file)
        if args.dataset_file is not None
        else None
    )
    summary = benchmark_submission(
        submission,
        cases=cases,
        num_samples=args.num_samples,
        noise_multiplier=args.noise_multiplier,
        max_resample_fits=args.max_resamples,
    )
    result = BenchmarkResult(
        task_name="gsfit_2pt",
        submission_name=submission.meta.name,
        score=summary["score"],
        metrics=summary,
    )
    path = save_result(result, args.output_dir)

    print(
        json.dumps(
            {
                "task": result.task_name,
                "submission": result.submission_name,
                "score": result.score,
                "output": str(path),
                "metrics": result.metrics,
            },
            indent=2,
        )
    )


if __name__ == "__main__":
    main()
