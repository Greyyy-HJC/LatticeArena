"""Benchmark driver for the two_pt_gsfit task."""

from __future__ import annotations

import argparse
import importlib
import json
from pathlib import Path
import sys

REPO_ROOT = Path(__file__).resolve().parents[3]
if str(REPO_ROOT) not in sys.path:
    sys.path.insert(0, str(REPO_ROOT))

from latticearena.leaderboard import save_result
from latticearena.task import BenchmarkResult

from tasks.two_pt_gsfit.benchmark.core import benchmark_submission, load_synthetic_cases
from tasks.two_pt_gsfit.interface import Pion2PtGroundStateFit


def load_submission(operator_name: str) -> Pion2PtGroundStateFit:
    """Import and instantiate a submission from operators/<name>.py."""

    module = importlib.import_module(f"tasks.two_pt_gsfit.operators.{operator_name}")
    for value in module.__dict__.values():
        if (
            isinstance(value, type)
            and issubclass(value, Pion2PtGroundStateFit)
            and value is not Pion2PtGroundStateFit
        ):
            return value()
    raise ValueError(f"No Pion2PtGroundStateFit implementation found in operator '{operator_name}'.")


def main() -> None:
    parser = argparse.ArgumentParser(description="Run the two_pt_gsfit benchmark")
    parser.add_argument("--operator", type=str, required=True, help="Operator module name under operators/")
    parser.add_argument("--num-samples", type=int, default=24, help="Synthetic resamples per benchmark case")
    parser.add_argument(
        "--max-resamples",
        type=int,
        default=None,
        help="Limit the number of resample refits used for the uncertainty estimate",
    )
    parser.add_argument(
        "--noise-multiplier",
        type=float,
        default=1.0,
        help="Global multiplier for the synthetic benchmark noise level",
    )
    parser.add_argument(
        "--dataset-file",
        type=Path,
        default=None,
        help="Optional .npz archive generated by scripts/generate_fake_data.py",
    )
    parser.add_argument(
        "--output-dir",
        type=Path,
        default=Path("tasks/two_pt_gsfit/benchmark/results"),
        help="Directory to save the benchmark JSON result",
    )
    args = parser.parse_args()

    submission = load_submission(args.operator)
    cases = load_synthetic_cases(args.dataset_file) if args.dataset_file is not None else None
    summary = benchmark_submission(
        submission,
        cases=cases,
        num_samples=args.num_samples,
        noise_multiplier=args.noise_multiplier,
        max_resample_fits=args.max_resamples,
    )
    result = BenchmarkResult(
        task_name="two_pt_gsfit",
        operator_name=submission.meta.name,
        score=summary["score"],
        metrics=summary,
    )
    path = save_result(result, args.output_dir)

    print(
        json.dumps(
            {
                "task": result.task_name,
                "operator": result.operator_name,
                "score": result.score,
                "output": str(path),
                "metrics": result.metrics,
            },
            indent=2,
        )
    )


if __name__ == "__main__":
    main()
